package chapter09;

public class User {

	public int userIdx;
	public String Id;
	public User(int userIdx, String id) {
		this.userIdx = userIdx;
		Id = id;
	}
	public int getUserIdx() {
		return userIdx;
	}
	public void setUserIdx(int userIdx) {
		this.userIdx = userIdx;
	}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	
	
	
	
	
	
	
}
